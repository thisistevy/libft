#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*cast;

	cast = (char *)str;
	i = 0;
	while (i < n)
	{
		cast[i] = c;
		i++;
	}
	return (cast);
}
